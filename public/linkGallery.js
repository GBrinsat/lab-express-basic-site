document.querySelector("#home").addEventListener("mouseover", () => {
    document.querySelector("#home").style.color = "rgb(255,187,0)"
    document.querySelector("#home").style.background = "black"
    document.querySelector("#home").style.borderRadius = "12px"
})

document.querySelector("#home").addEventListener("mouseout", () => {
    document.querySelector("#home").style.color = "black"
    document.querySelector("#home").style.background = "rgb(255,187,0)"
    document.querySelector("#home").style.borderRadius = "0px"
})

